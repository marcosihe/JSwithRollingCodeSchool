let numero = parseInt(prompt("Ingrese un número"));

if (numero % 2 == 0) {
    document.write("El número ingresado: " + numero + ", es divisible por 2");
}
if (numero % 3 == 0) {
    document.write("<br>El número ingresado: " + numero + ", es divisible por 3");
}
if (numero % 5 == 0) {
    document.write("<br>El número ingresado: " + numero + ", es divisible por 5");
}
if (numero % 7 == 0) {
    document.write("<br>El número ingresado: " + numero + ", es divisible por 7");
}